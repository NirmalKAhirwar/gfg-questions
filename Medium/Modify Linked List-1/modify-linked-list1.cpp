class Solution{
    public:
    struct Node* modifyTheList(struct Node *head)
    {
        //add code here.
        vector<int> val;
        struct Node* temp=head;
        while(temp){
            val.push_back(temp->data);
            temp=temp->next;
        }
        struct Node* tem=head;
        for(int i=val.size()-1;i>=0;i--){
            if(i>(val.size()-1)/2){
                tem->data=val[i]-tem->data;
                tem=tem->next;
            }
            else{
                tem->data=val[i];
                tem=tem->next;
            }
        }
        return head;
    }
};

