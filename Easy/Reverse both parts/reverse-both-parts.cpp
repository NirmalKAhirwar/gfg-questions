class Solution
{
public:
   
    Node *reverse(Node *head, int k)
    {
        // code here
        int n = 0;

        Node *pre = NULL;

        Node *forward = NULL;

        Node *curr = head;

 

// First reversing the first part

        while(curr != NULL and n < k){

            forward = curr->next;

            curr->next = pre;

            pre = curr;

            curr = forward;

            n++;

        }

        

        Node *ansHead = pre;

        pre = NULL;

        forward = NULL;

// Here we are reversing the second part of list

        while(curr != NULL){

            forward = curr->next;

            curr->next = pre;

            pre = curr;

            curr = forward;

        }

        head->next = pre;

        return ansHead;

    
    }
};

